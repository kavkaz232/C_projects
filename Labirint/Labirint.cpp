void initMaze(short mazeType) {
    clear;

    switch (mazeType)
    {
    case 1:
        for (int i = 0; i < MAZE15_ROWS; i++) {
            for (int j = 0; j < MAZE15_COLUMNS; j++) {
                if (maze15[i][j] == 1) {
                    out << "# ";
                }
                else {
                    out << "  ";
                }
            }
            out << "\n";
        }
        break;
    case 2:
        for (int i = 0; i < MAZE20_ROWS; i++) {
            for (int j = 0; j < MAZE20_COLUMNS; j++) {
                if (maze20[i][j] == 1) {
                    out << "# ";
                }
                else {
                    out << "  ";
                }
            }
            out << "\n";
        }
        break;
    case 3:
        for (int i = 0; i < MAZE30_ROWS; i++) {
            for (int j = 0; j < MAZE30_COLUMNS; j++) {
                if (maze30[i][j] == 1) {
                    out << "# ";
                }
                else {
                    out << "  ";
                }
            }
            out << "\n";
        }
        break;
    }
}

